#include "libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void*, size_t))
{
	if (alst && *alst)
	{
		(*del)((*alst)->cnt, (*alst)->content_size);
		free(*alst);
		*alst = NULL;
	}
}
